public class For_homework {

	public static void main(String[] args) {
		
		// 0  1  2  --> 3ë²ˆ  
		for (int i = 0; i < 3; i++) {
			System.out.print(i + "\t");
		}
		System.out.println();
		
		for (int i = 0; i < 3; i++) {
			System.out.print(i + "\t");
		}
		System.out.println(); 
		
		for (int i = 0; i < 3; i++) {
			System.out.print(i + "\t");
		}
		System.out.println(); 
		
		
		System.out.println("-----------------------");
		
		for (int a = 0; a < 3; a++) {
			for (int i = 0; i < 3; i++) {
				System.out.print(i + "\t");
			}
			System.out.println(); 
		}
		
		
		
	} // end of main

} // end of class
